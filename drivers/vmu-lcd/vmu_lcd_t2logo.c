static void usage(const char *prog)
{
	fprintf(stderr,
		"usage: %s [-o DEV] [-r]\n"
		"  -o DEV   output device (default /dev/vmu_lcd0)\n"
		"  -r       rotate 180 degrees (VMU mounted in a controller)\n",
		prog);
}

int main(int argc, char **argv)
{
	const char *dev = "/dev/vmu_lcd0";
	int flip = 0;
	int fd, i;
	ssize_t n;

	for (i = 1; i < argc; i++) {
		if (!strcmp(argv[i], "-o") && i + 1 < argc) {
			dev = argv[++i];
		} else if (!strcmp(argv[i], "-r")) {
			flip = 1;
		} else if (!strcmp(argv[i], "-h") || !strcmp(argv[i], "--help")) {
			usage(argv[0]);
			return 0;
		} else {
			fprintf(stderr, "%s: unknown argument '%s'\n",
				argv[0], argv[i]);
			usage(argv[0]);
			return 1;
		}
	}

	if (flip)
		vmu_lcd_flip180(t2logo);

	fd = open(dev, O_WRONLY);
	if (fd < 0) {
		fprintf(stderr, "%s: %s: %s\n", argv[0], dev, strerror(errno));
		return 1;
	}

	n = write(fd, t2logo, LCD_FB_SIZE);
	close(fd);

	if (n != LCD_FB_SIZE) {
		if (n < 0)
			fprintf(stderr, "%s: write: %s\n",
				argv[0], strerror(errno));
		else
			fprintf(stderr, "%s: short write (%zd/%d)\n",
				argv[0], n, LCD_FB_SIZE);
		return 1;
	}

	return 0;
}
