/*
 * vmu_lcd_t2logo - show the T2 Linux logo on the SEGA Dreamcast VMU LCD.
 *
 * The monochrome "T/2" mark (with underline) is baked in as a 192-byte VMU
 * framebuffer (48x32, 1bpp, 6 bytes per row, MSB = leftmost pixel, row 0 at
 * the top). The tool simply writes it once to the display.
 *
 * The bitmap was derived from https://t2linux.com/images/t2logo.png, scaled
 * to fit and thresholded on the artwork's alpha channel.
 *
 * Examples:
 *
 *     vmu_lcd_t2logo                     # draw on /dev/vmu_lcd0
 *     vmu_lcd_t2logo -o /dev/vmu_lcd1    # draw on a second VMU
 *     vmu_lcd_t2logo -r                  # rotate 180 (VMU in a controller)
 *
 * This program is free software; you can redistribute it and/or modify it
 * under the terms of the GNU General Public License version 2.
 */

